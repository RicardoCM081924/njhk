package com.hhu.model;

public class OriginalPhoto {
    private String photo_name;
    private String time;
    private String row_x;
    private String row_y;
    private String row_photo_path;
    private String layer_num;

    public String getPhoto_name() {
        return photo_name;
    }

    public void setPhoto_name(String photo_name) {
        this.photo_name = photo_name;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getRow_x() {
        return row_x;
    }

    public void setRow_x(String row_x) {
        this.row_x = row_x;
    }

    public String getRow_y() {
        return row_y;
    }

    public void setRow_y(String row_y) {
        this.row_y = row_y;
    }

    public String getLayer_num() {
        return layer_num;
    }

    public void setLayer_num(String layer_num) {
        this.layer_num = layer_num;
    }

    public String getRow_photo_path() {
        return row_photo_path;
    }

    public void setRow_photo_path(String row_photo_path) {
        this.row_photo_path = row_photo_path;
    }
}
